package main

import "fmt"

func main() {

	var initialValue string = "initial"
	fmt.Println(initialValue)

	var secondValue, thirdValue int = 1, 2
	fmt.Println(secondValue, thirdValue)

	var boolValue bool = true
	fmt.Println(boolValue)

	var defaultValueVar int
	fmt.Println(defaultValueVar)

	shortHandVar := "apple"
	fmt.Println(shortHandVar)
}
